    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nim = txtNIM.getText().trim();
    String nama = txtNama.getText().trim();
    String jurusan = cboJurusan.getSelectedItem().toString();

    if (nim.equals("") || nama.equals("")) {
        JOptionPane.showMessageDialog(null, "NIM dan Nama tidak boleh kosong");
        return;
    }

    try {
        Statement statement = koneksi.getConnection().createStatement();
        String sql = "INSERT INTO mahasiswa VALUES ('" + nim + "', '" + nama + "', '" + jurusan + "')";
        statement.executeUpdate(sql);
        statement.close();

        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        tampildata();
        bersih();
    } catch (Exception t) {
        JOptionPane.showMessageDialog(null, "Data gagal disimpan\n" + t.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

