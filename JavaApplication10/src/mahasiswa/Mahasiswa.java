    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    String nim = txtNIM.getText().trim();

    if (nim.equals("")) {
        JOptionPane.showMessageDialog(null, "Silakan pilih data yang ingin dihapus");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = koneksi.getConnection();
            Statement statement = conn.createStatement();
            String sql = "DELETE FROM mahasiswa WHERE nim = '" + nim + "'";
            int hasil = statement.executeUpdate(sql);

            if (hasil > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                tampildata();
                bersih();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus, NIM tidak ditemukan");
            }

            statement.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menghapus data\n" + e.getMessage());
        }
    }   
    }//GEN-LAST:event_jButton3ActionPerformed

