public void tampildata() {
    DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("NIM");
    tbl.addColumn("Nama");
    tbl.addColumn("Jurusan");
    jTable1.setModel(tbl); // tblMahasiswa

    try {
        Statement statement = (Statement) koneksi.getConnection().createStatement();
        ResultSet res = statement.executeQuery("SELECT * FROM mahasiswa");
        while (res.next()) {
            tbl.addRow(new Object[]{
                res.getString("nim"),
                res.getString("nama"),
                res.getString("jurusan")
            });
        }
        jTable1.setModel(tbl);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Terjadi kesalahan saat mengambil data dari database");
    }
}


